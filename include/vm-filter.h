#ifndef VM_FILTER_H
#define VM_FILTER_H

#include <stdint.h>

typedef uint32_t vm_filter_average_t;
typedef uint8_t  vm_filter_exponent_t;

typedef struct vm_filter {
  vm_filter_average_t  average;
  vm_filter_exponent_t exponent;
} vm_filter_t;

void vm_filter_init(vm_filter_t *, vm_filter_exponent_t);
vm_filter_average_t vm_filter_get(const vm_filter_t *);
void vm_filter_put(vm_filter_t *, vm_filter_average_t);

#endif /* !VM_FILTER_H */
